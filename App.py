import streamlit as st

st.set_page_config(page_title="حاسبة المعدل - الثانية باك", page_icon="📊", layout="centered")

st.title("📊 حاسبة المعدل - الثانية باكالوريا")
st.write("احسب المعدل العام + نقطة الامتحان الوطني على 20")

tab1, tab2 = st.tabs(["📝 المعدل العام", "🎓 الامتحان الوطني"])

# تبويب 1: المعدل العام
with tab1:
    st.subheader("المعدل العام لجميع المواد")
    st.caption("دخل النقطة والمعامل ديال كل مادة")
    
    مواد_العامة = [
        "علوم الحياة والأرض",
        "اللغة العربية",
        "اللغة الفرنسية", 
        "التربية الإسلامية",
        "المعلوميات",
        "الإجتماعيات",
        "الفلسفة",
        "التربية البدنية",
        "اللغة الإنجليزية"
    ]

    مجموع_النقاط = 0
    مجموع_المعاملات = 0

    for مادة in مواد_العامة:
        col1, col2 = st.columns([2, 1])
        with col1:
            نقطة = st.number_input(f"نقطة {مادة}", min_value=0.0, max_value=20.0, step=0.25, key=f"عام_{مادة}")
        with col2:
            معامل = st.number_input(f"المعامل", min_value=1, max_value=9, value=1, step=1, key=f"معامل_عام_{مادة}")
        
        مجموع_النقاط += نقطة * معامل
        مجموع_المعاملات += معامل

    st.markdown("---")
    if مجموع_المعاملات > 0:
        المعدل = مجموع_النقاط / مجموع_المعاملات
        st.metric(label="المعدل العام", value=f"{المعدل:.2f} / 20")
        
        if المعدل >= 16: 
            st.balloons()
            st.success("ممتاز! تبارك الله عليك 🎉")
        elif المعدل >= 14: st.success("حسن جدا 👍")
        elif المعدل >= 12: st.info("حسن 🙂")
        elif المعدل >= 10: st.warning("مقبول - تقدر تحسن")
        else: st.error("راسب - خاصك تخدم كثر 💪")
    else:
        st.info("دخل النقط والمعاملات باش نحسب ليك المعدل")

# تبويب 2: الامتحان الوطني
with tab2:
    st.subheader("نقطة الامتحان الوطني")
    
    شعبة = st.radio(
        "اختار الشعبة ديالك:", 
        ["علمي - علوم الحياة والأرض", "أدبي - آداب وعلوم إنسانية"], 
        horizontal=True
    )
    
    st.markdown("---")
    
    if شعبة == "علمي - علوم الحياة والأرض":
        st.write("**مواد الوطني شعبة علوم الحياة والأرض:**")
        مواد_الوطني = {
            "الرياضيات": 7,
            "الفيزياء والكيمياء": 7,
            "علوم الحياة والأرض": 5,
            "اللغة الإنجليزية": 2,
            "الفلسفة": 2
        }
    else:  # أدبي
        st.write("**مواد الوطني شعبة الآداب والعلوم الإنسانية:**")
        مواد_الوطني = {
            "اللغة العربية": 4,
            "الإجتماعيات": 4,
            "الفلسفة": 3,
            "اللغة الإنجليزية": 4
        }
    
    مجموع_وطني = 0
    مجموع_معاملات_وطني = 0
    
    for مادة, معامل in مواد_الوطني.items():
        نقطة = st.number_input(
            f"{مادة}", 
            min_value=0.0, 
            max_value=20.0, 
            step=0.25, 
            help=f"المعامل: {معامل}",
            key=f"وطني_{مادة}"
        )
        مجموع_وطني += نقطة * معامل
        مجموع_معاملات_وطني += معامل
    
    st.markdown("---")
    
    if مجموع_معاملات_وطني > 0:
        معدل_الوطني = مجموع_وطني / مجموع_معاملات_وطني
        st.metric(label=f"معدل الامتحان الوطني", value=f"{معدل_الوطني:.2f} / 20")
        
        if معدل_الوطني >= 16: 
            st.balloons()
            st.success("ممتاز في الوطني! 🎉")
        elif معدل_الوطني >= 14: st.success("حسن جدا في الوطني 👍")
        elif معدل_الوطني >= 12: st.info("حسن في الوطني 🙂")
        elif معدل_الوطني >= 10: st.warning("مقبول في الوطني")
        else: st.error("راسب في الوطني - شد حيلك 💪")

st.markdown("---")
st.caption("صاوبو عبد الله ❤️ | حاسبة خاصة بتلاميذ الثانية باك - المغرب")
