import streamlit as st
from fpdf import FPDF
import datetime

st.set_page_config(page_title="حاسبة الباك الشاملة", page_icon="🎓", layout="centered")

st.title("🎓 حاسبة الباكالوريا الشاملة")
st.write("المعدل العام + الوطني + النهائي + كشف نقط PDF")

tab1, tab2, tab3 = st.tabs(["📝 المعدل العام", "🎯 الامتحان الوطني", "🏆 النتيجة النهائية + PDF"])

with tab1:
    st.subheader("المعدل العام لجميع المواد")
    مواد_العامة = ["علوم الحياة والأرض", "اللغة العربية", "اللغة الفرنسية", "التربية الإسلامية", "المعلوميات", "الإجتماعيات", "الفلسفة", "التربية البدنية", "اللغة الإنجليزية"]
    مجموع_النقاط = 0
    مجموع_المعاملات = 0
    تفاصيل_عام = []
    for مادة in مواد_العامة:
        col1, col2 = st.columns([2, 1])
        with col1:
            نقطة = st.number_input(f"نقطة {مادة}", 0.0, 20.0, 0.0, 0.25, key=f"عام_{مادة}")
        with col2:
            معامل = st.number_input(f"المعامل", 1, 9, 1, 1, key=f"معامل_عام_{مادة}")
        مجموع_النقاط += نقطة * معامل
        مجموع_المعاملات += معامل
        if نقطة > 0: تفاصيل_عام.append([مادة, نقطة, معامل])
    if مجموع_المعاملات > 0:
        المعدل = مجموع_النقاط / مجموع_المعاملات
        st.session_state['المعدل_العام'] = المعدل
        st.session_state['تفاصيل_عام'] = تفاصيل_عام
        st.metric("المعدل العام", f"{المعدل:.2f} / 20")

with tab2:
    st.subheader("نقطة الامتحان الوطني")
    شعبة = st.radio("اختار الشعبة:", ["علمي - علوم الحياة والأرض", "علمي - علوم فيزيائية", "علمي - علوم رياضية أ", "أدبي - آداب وعلوم إنسانية"])
    if "الحياة والأرض" in شعبة:
        مواد_الوطني = {"الرياضيات": 7, "الفيزياء والكيمياء": 7, "علوم الحياة والأرض": 5, "الفلسفة": 2, "اللغة الإنجليزية": 2}
    elif "فيزيائية" in شعبة:
        مواد_الوطني = {"الرياضيات": 7, "الفيزياء والكيمياء": 7, "علوم الحياة والأرض": 3, "الفلسفة": 2, "اللغة الإنجليزية": 2}
    elif "رياضية" in شعبة:
        مواد_الوطني = {"الرياضيات": 9, "الفيزياء والكيمياء": 7, "علوم الحياة والأرض": 3, "الفلسفة": 2, "اللغة الإنجليزية": 2}
    else:
        مواد_الوطني = {"اللغة العربية": 4, "الإجتماعيات": 4, "الفلسفة": 3, "اللغة الإنجليزية": 4}
    مجموع_وطني = 0
    مجموع_معاملات_وطني = 0
    تفاصيل_وطني = []
    for مادة, معامل in مواد_الوطني.items():
        نقطة = st.number_input(f"{مادة}", 0.0, 20.0, 0.0, 0.25, help=f"المعامل: {معامل}", key=f"وطني_{مادة}")
        مجموع_وطني += نقطة * معامل
        مجموع_معاملات_وطني += معامل
        if نقطة > 0: تفاصيل_وطني.append([مادة, نقطة, معامل])
    if مجموع_معاملات_وطني > 0:
        معدل_الوطني = مجموع_وطني / مجموع_معاملات_وطني
        st.session_state['معدل_الوطني'] = معدل_الوطني
        st.session_state['تفاصيل_وطني'] = تفاصيل_وطني
        st.session_state['شعبة'] = شعبة
        st.metric("معدل الامتحان الوطني", f"{معدل_الوطني:.2f} / 20")

with tab3:
    st.subheader("🏆 المعدل النهائي + كشف النقط")
    col1, col2 = st.columns(2)
    with col1:
        معدل_المراقبة = st.number_input("معدل المراقبة المستمرة 25%", 0.0, 20.0, 0.0, 0.01, key="مراقبة")
    with col2:
        معدل_الجهوي = st.number_input("معدل الامتحان الجهوي 25%", 0.0, 20.0, 0.0, 0.01, key="جهوي")
    معدل_الوطني_محفوظ = st.session_state.get('معدل_الوطني', 0.0)
    if معدل_الوطني_محفوظ > 0:
        st.info(f"معدل الوطني 50%: {معدل_الوطني_محفوظ:.2f} / 20")
    else:
        معدل_الوطني_محفوظ = st.number_input("معدل الامتحان الوطني 50%", 0.0, 20.0, 0.0, 0.01, key="وطني_يدوي")
    if معدل_المراقبة > 0 and معدل_الجهوي > 0 and معدل_الوطني_محفوظ > 0:
        المعدل_النهائي = (معدل_المراقبة * 0.25) + (معدل_الجهوي * 0.25) + (معدل_الوطني_محفوظ * 0.50)
        st.markdown("### 🎯 النتيجة النهائية:")
        col1, col2, col3 = st.columns(3)
        col1.metric("المراقبة", f"{معدل_المراقبة:.2f}")
        col2.metric("الجهوي", f"{معدل_الجهوي:.2f}")
        col3.metric("الوطني", f"{معدل_الوطني_محفوظ:.2f}")
        st.metric("**المعدل النهائي ديال الباك**", f"{المعدل_النهائي:.2f} / 20")
        if المعدل_النهائي >= 10:
            st.balloons()
            if المعدل_النهائي >= 16: ميزة = "حسن جدا"; st.success(f"🎉 مبروووك! ناجح بميزة {ميزة} 🏅")
            elif المعدل_النهائي >= 14: ميزة = "حسن"; st.success(f"🎉 مبروووك! ناجح بميزة {ميزة} 👍")
            elif المعدل_النهائي >= 12: ميزة = "مستحسن"; st.info(f"🎉 مبروووك! ناجح بميزة {ميزة} 🙂")
            else: ميزة = "مقبول"; st.info(f"🎉 مبروووك! ناجح بميزة {ميزة}")
        else:
            ميزة = "راسب"; st.error(f"❌ للأسف راسب - خاصك {10 - المعدل_النهائي:.2f} نقطة")
        st.markdown("---")
        class PDF(FPDF):
            def header(self):
                self.set_font('Arial', 'B', 16)
                self.cell(0, 10, 'كشف النقط - الباكالوريا المغربية', 0, 1, 'C')
                self.ln(5)
        def create_full_pdf():
            pdf = PDF()
            pdf.add_page()
            pdf.set_font('Arial', '', 12)
            pdf.cell(0, 10, f"التاريخ: {datetime.date.today()}", 0, 1, 'R')
            pdf.cell(0, 10, f"الشعبة: {st.session_state.get('شعبة', 'غير محددة')}", 0, 1, 'R')
            pdf.ln(5)
            pdf.set_font('Arial', 'B', 14)
            pdf.cell(0, 10, f"المعدل النهائي: {المعدل_النهائي:.2f} / 20", 0, 1, 'C')
            pdf.cell(0, 10, f"النتيجة: {ميزة}", 0, 1, 'C')
            pdf.ln(10)
            pdf.set_font('Arial', 'B', 12)
            pdf.cell(0, 10, 'تفاصيل المعدل النهائي:', 0, 1)
            pdf.set_font('Arial', '', 11)
            pdf.cell(0, 8, f"1. المراقبة المستمرة: {معدل_المراقبة:.2f} x 25% = {mعدل_المراقبة * 0.25:.2f}", 0, 1)
            pdf.cell(0, 8, f"2. الامتحان الجهوي: {mعدل_الجهوي:.2f} x 25% = {mعدل_الجهوي * 0.25:.2f}", 0, 1)
            pdf.cell(0, 8, f"3. الامتحان الوطني: {mعدل_الوطني_محفوظ:.2f} x 50% = {mعدل_الوطني_محفوظ * 0.50:.2f}", 0, 1)
            return pdf.output(dest='S').encode('latin-1')
        pdf_bytes = create_full_pdf()
        st.download_button("📄 حمل كشف النقط PDF", pdf_bytes, f"نتيجة_الباك_{datetime.date.today()}.pdf", "application/pdf", use_container_width=True)

st.markdown("---")
st.caption("صاوبو عبد الله ❤️ | النسخة النهائية 2026")
